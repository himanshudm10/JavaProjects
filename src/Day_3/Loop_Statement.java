package Day_3;

public class Loop_Statement 
{

	public static void main(String[] args) 
	{
		
		//Print Numbers from 1 to 10 using WHILE LOOP
		int i=1;
		while(i<11)
		{
			System.out.println(i);
			i++;
		}
		
		// Print numbers between 11 to 20 using FOR LOOP
		
		for(i=10; i<21;i++)
		{
			System.out.println(i);
		}
		
		// Print numbers between 21 to 30 using FOR LOOP
		
		do
		{
			System.out.println(i);
			i++;
		}while(i<30);
		
	}

}
